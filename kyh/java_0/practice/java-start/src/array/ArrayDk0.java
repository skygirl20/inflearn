package array;

public class ArrayDk0 {
    public static void main(String[] args) {
        // 2X3 2차원 배열을 만든다.
         int[][] arr = new int[2][3]; // 행2, 열3

        arr[0][0] = 1; //0행, 0열
        arr[0][1] = 2; //0행, 1열
        arr[0][2] = 3; //0행, 2열
        arr[1][0] = 4; //1행, 0열
        arr[1][1] = 5; //1행, 1열
        arr[1][2] = 6; //1행, 2열
    }
}
